// WRITE A CODE TO PRINT 1 TO 5 & A TO Z USING INCREMENT AND DECREMENT OPERTAOR
public class Q2_Print1To5AndAToZ {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            System.out.print(i + " ");
        }
        System.out.println();
        for (char ch = 'a'; ch <= 'z'; ch++) {
            System.out.print(ch + " ");
        }
    }
}
